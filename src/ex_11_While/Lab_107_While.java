package ex_11_While;

public class Lab_107_While {
    public static void main(String[] args) {

        int i = 0; // Init
        while (i < 10) {//condition
            System.out.print(i + "\t");
            i++; // Updation
        }

        //  I, C, U
        for (int j = 0; j < 10 ; j++) {
            System.out.println(j);
        }
    }
}
